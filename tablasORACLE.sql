CREATE TABLE ESTIMACION_DEMANDA (
    FECHA DATE PRIMARY KEY,
    CUOTA_PRODUCCION NUMBER(10,0) DEFAULT 0 NOT NULL CHECK (CUOTA_PRODUCCION <= 100),
    TEMP NUMBER(5,2) NOT NULL CHECK (TEMP >= 0 AND TEMP <= 50),
    DEMANDA NUMBER(10,2) DEFAULT 0 NOT NULL CHECK (DEMANDA > 0)
);


CREATE TABLE EMPRESA (
    CIF VARCHAR2(9) DEFAULT '00000000A' PRIMARY KEY CHECK (LENGTH(CIF) = 9 AND SUBSTR(CIF, 1, 1) = 'A'),
    NOMBRE VARCHAR2(50) DEFAULT 'Empresa sin nombre' NOT NULL CHECK (LENGTH(NOMBRE) >= 3)
);


CREATE TABLE CENTRALES (
    COD_CENTRAL VARCHAR2(10) PRIMARY KEY,
    CIF VARCHAR2(9) DEFAULT '00000000A',
    FECHA DATE,
    UBICACION VARCHAR2(50) DEFAULT 'Sin especificar' NOT NULL CHECK (LENGTH(UBICACION) BETWEEN 2 AND 50),
    CAPACIDAD_MAX NUMBER(10,2) NOT NULL CHECK (CAPACIDAD_MAX >= 0),
    CONSTRAINT FK_EMPRESA_CENTRAL FOREIGN KEY (CIF) REFERENCES EMPRESA(CIF),
    CONSTRAINT FK_ESTIMACION_DEMANDA_CENTRAL FOREIGN KEY (FECHA) REFERENCES ESTIMACION_DEMANDA(FECHA)
);


